export function gregorianToVikramSamvat(date) {
  // Approximate conversion: V.S. = G.Y. + 56 or 57 depending on month
  const year = date.getFullYear();
  const month = date.getMonth(); // 0-based
  return month < 3 ? year + 56 : year + 57;
}
